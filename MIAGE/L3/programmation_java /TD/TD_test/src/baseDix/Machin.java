package baseDix;

public class Machin {
	/**
	 * Cette méthode doit exprimer la valeur numérique entière de la multiplication par 10 d'une valeur additionnée à une autre
	 * @param a un entier sur un octet
	 * @param b un entier sur un octet
	 * @return int la valeur numérique de l'entier a*10+b
	 */
	public static int baseDix(Byte a, Byte b) {
		return ((a*10)+b);
	}
}
